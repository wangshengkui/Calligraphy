package com.jinke.calligraphy.backup;

public class CalligraphyItem {

	private int templateID;
	private int pageNum;
	private int availableID;
	private int itemID;
	private int flipBottom;
	private int flipDst;
	private String charType;
	private String matrix;
	private String created;
	private String byteBitmap;
	private String uri;
	public int getTemplateID() {
		return templateID;
	}
	public void setTemplateID(int templateID) {
		this.templateID = templateID;
	}
	public int getPageNum() {
		return pageNum;
	}
	public void setPageNum(int pageNum) {
		this.pageNum = pageNum;
	}
	public int getAvailableID() {
		return availableID;
	}
	public void setAvailableID(int availableID) {
		this.availableID = availableID;
	}
	public int getItemID() {
		return itemID;
	}
	public void setItemID(int itemID) {
		this.itemID = itemID;
	}
	public int getFlipBottom() {
		return flipBottom;
	}
	public void setFlipBottom(int flipBottom) {
		this.flipBottom = flipBottom;
	}
	public int getFlipDst() {
		return flipDst;
	}
	public void setFlipDst(int flipDst) {
		this.flipDst = flipDst;
	}
	public String getCharType() {
		return charType;
	}
	public void setCharType(String charType) {
		this.charType = charType;
	}
	public String getMatrix() {
		return matrix;
	}
	public void setMatrix(String matrix) {
		this.matrix = matrix;
	}
	public String getCreated() {
		return created;
	}
	public void setCreated(String created) {
		this.created = created;
	}
	public String getByteBitmap() {
		return byteBitmap;
	}
	public void setByteBitmap(String byteBitmap) {
		this.byteBitmap = byteBitmap;
	}
	public String getUri() {
		return uri;
	}
	public void setUri(String uri) {
		this.uri = uri;
	}
	
	
	
}
