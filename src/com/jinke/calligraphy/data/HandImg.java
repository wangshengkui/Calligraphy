package com.jinke.calligraphy.data;

import android.graphics.Rect;

public class HandImg {
	int 	form;
	int 	layout;
	int 	hid;
	Rect 	area;
	int 	chartype;
	int 	level;
	long 	imgtime;
	int 	sequence;
	int	 	imgvector;
	Img  	img;
	public int getForm() {
		return form;
	}
	public void setForm(int form) {
		this.form = form;
	}
	public int getLayout() {
		return layout;
	}
	public void setLayout(int layout) {
		this.layout = layout;
	}
	public int getHid() {
		return hid;
	}
	public void setHid(int hid) {
		this.hid = hid;
	}
	public Rect getArea() {
		return area;
	}
	public void setArea(Rect area) {
		this.area = area;
	}
	public int getChartype() {
		return chartype;
	}
	public void setChartype(int chartype) {
		this.chartype = chartype;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public long getImgtime() {
		return imgtime;
	}
	public void setImgtime(long imgtime) {
		this.imgtime = imgtime;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public int getImgvector() {
		return imgvector;
	}
	public void setImgvector(int imgvector) {
		this.imgvector = imgvector;
	}
	public Img getImg() {
		return img;
	}
	public void setImg(Img img) {
		this.img = img;
	}
	
	
	
}